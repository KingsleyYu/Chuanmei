function EditTemplate() {
    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
     继承于Controller基类
     -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    Controller.call(this);

    /*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
    点击切换显示详细信息
    -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
    this.initAccording();
};



/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 类的初始化
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
EditTemplate.prototype.initAccording = function() {
    $("div.info").find("dt").on("click", function() {
        var _this = $(this);


        if (_this.parent().hasClass('on')) {
        	 _this.parent().removeClass('on');
            _this.find("a").html("+");

        } else {
            $("div.info").find("dl").removeClass('on').find("dt a").html("+");

            _this.parent().addClass('on');
            _this.find("a").html("-");
        }

    })
}

/*-----------------------------------------------------------------------------------------------------------------------------------------------------------------------
 类的初始化
 -----------------------------------------------------------------------------------------------------------------------------------------------------------------------*/
$(document).ready(function() {
    new EditTemplate;
});
